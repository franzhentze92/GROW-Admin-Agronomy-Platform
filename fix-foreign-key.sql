-- Check the current foreign key constraint
SELECT 
    tc.constraint_name, 
    tc.table_name, 
    kcu.column_name, 
    ccu.table_name AS foreign_table_name,
    ccu.column_name AS foreign_column_name 
FROM 
    information_schema.table_constraints AS tc 
    JOIN information_schema.key_column_usage AS kcu
      ON tc.constraint_name = kcu.constraint_name
      AND tc.table_schema = kcu.table_schema
    JOIN information_schema.constraint_column_usage AS ccu
      ON ccu.constraint_name = tc.constraint_name
      AND ccu.table_schema = tc.table_schema
WHERE tc.constraint_type = 'FOREIGN KEY' 
    AND tc.table_name='field_trials'
    AND kcu.column_name = 'responsible_agronomist_id';

-- Drop the existing foreign key constraint
ALTER TABLE field_trials DROP CONSTRAINT IF EXISTS field_trials_responsible_agronomist_id_fkey;

-- Add the correct foreign key constraint pointing to public.users
ALTER TABLE field_trials 
ADD CONSTRAINT field_trials_responsible_agronomist_id_fkey 
FOREIGN KEY (responsible_agronomist_id) 
REFERENCES public.users(id);

